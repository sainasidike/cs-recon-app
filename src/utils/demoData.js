function buildExcelBlob(sheetName, headers, rows) {
  const ws = XLSX.utils.aoa_to_sheet([headers, ...rows]);
  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, sheetName);
  const buf = XLSX.write(wb, { type: 'array', bookType: 'xlsx' });
  return new Blob([buf], { type: 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet' });
}

function buildCsvBlob(headers, rows) {
  const lines = [headers.join(',')];
  for (const row of rows) {
    lines.push(row.map(c => {
      const s = String(c ?? '');
      return s.includes(',') ? `"${s}"` : s;
    }).join(','));
  }
  return new Blob([lines.join('\n')], { type: 'text/csv' });
}

export function getDemoList() {
  return DEMOS.map(d => ({ id: d.id, name: d.name, desc: d.desc, fileCount: d.files.length, fileNames: d.files.map(f => f.name) }));
}

export function buildDemoFiles(demoId) {
  const demo = DEMOS.find(d => d.id === demoId);
  if (!demo) return [];

  return demo.files.map(f => {
    const isCSV = f.name.endsWith('.csv');
    const blob = isCSV
      ? buildCsvBlob(f.headers, f.rows)
      : buildExcelBlob(f.sheetName, f.headers, f.rows);
    const ext = isCSV ? '.csv' : '.xlsx';
    return new File([blob], f.name, { type: blob.type });
  });
}
